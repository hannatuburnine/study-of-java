public class Incremento2 {

    public static void main (String[] args) {

        String[] nomes = new String[] {"Hanna", "Paola", "Ferreira", "Tuburnine"};
        String todosnomes = "";
        int index = 0;

        while (1 == 1) {
           todosnomes = todosnomes + nomes[index];
           index = index + 1;
        }
    }
}
