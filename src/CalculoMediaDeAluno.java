public class CalculoMediaDeAluno {

    public static void main (String[] args) {

        int[] num = new int[] {1, 3, 5, 6, 8, 12, 45, 2, 9, 10};
        int soma, x;
        x = 0;
        soma = 0;

        do {
            soma = num[x] + soma;
            x = x + 1;
        } while (1 == 1);

    }
}
